package com.zuosuo.component.time;

public class DiscTime {
    private int year, month, day, hour, minute, second;

    public int getYear() {
        return year;
    }

    public DiscTime setYear(int year) {
        this.year = year;
        return this;
    }

    public int getMonth() {
        return month;
    }

    public DiscTime setMonth(int month) {
        this.month = month;
        return this;
    }

    public int getDay() {
        return day;
    }

    public DiscTime setDay(int day) {
        this.day = day;
        return this;
    }

    public int getHour() {
        return hour;
    }

    public DiscTime setHour(int hour) {
        this.hour = hour;
        return this;
    }

    public int getMinute() {
        return minute;
    }

    public DiscTime setMinute(int minute) {
        this.minute = minute;
        return this;
    }

    public int getSecond() {
        return second;
    }

    public DiscTime setSecond(int second) {
        this.second = second;
        return this;
    }
}
